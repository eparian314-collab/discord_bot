def get_quick_start_guide(language_code: str) -> dict:
    """
    Get the quick start guide for a specific language.
    
    Args:
        language_code: ISO 639-1 language code (e.g., 'es', 'fr', 'de')
        
    Returns:
        Dict with 'title' and 'content' keys, defaults to English if not found
    """
    return QUICK_START_GUIDES.get(language_code, QUICK_START_GUIDES["en"])
